#include<stdio.h>
void main(void)
{
	int res_type;

	switch (res_type)
	{
	case (res_type == 1):
	in_data();
	check();
	break;
	case (res_type == 2):
	capacity();
	devtype();
	break;
	case (res_type == 3):
	volume();
	mass();
	break;
	case (res_type == 4):
	area();
	weight();
	break;
	case (res_type == 5):
	files();
	saves();
	break;
	}
}
